export interface PincodeResult {
  lat: number; lng: number; city: string; state: string; valid: boolean;
}

export function detectLocationFromPincode(pincode: string): PincodeResult {
  const cleaned = pincode.replace(/\s/g, "");
  if (!/^\d{6}$/.test(cleaned)) return { lat: 0, lng: 0, city: "", state: "", valid: false };
  const p3 = cleaned.slice(0, 3);
  const p2 = cleaned.slice(0, 2);
  const match = PINCODE_MAP[p3] ?? PINCODE_MAP[p2];
  if (!match) return { lat: 0, lng: 0, city: "", state: "", valid: false };
  return { lat: match[0], lng: match[1], city: match[2], state: match[3], valid: true };
}

export function haversineKm(lat1: number, lng1: number, lat2: number, lng2: number): number {
  const R = 6371;
  const dLat = ((lat2 - lat1) * Math.PI) / 180;
  const dLng = ((lng2 - lng1) * Math.PI) / 180;
  const a = Math.sin(dLat / 2) ** 2 +
    Math.cos((lat1 * Math.PI) / 180) * Math.cos((lat2 * Math.PI) / 180) * Math.sin(dLng / 2) ** 2;
  return R * 2 * Math.atan2(Math.sqrt(a), Math.sqrt(1 - a));
}

export function calcShipping(distanceKm: number): { cost: number; days: number; label: string } {
  if (distanceKm < 100)  return { cost: 0,  days: 1, label: "Tomorrow by 9 PM" };
  if (distanceKm < 300)  return { cost: 29, days: 2, label: "In 2 days" };
  if (distanceKm < 700)  return { cost: 49, days: 3, label: "In 3–4 days" };
  if (distanceKm < 1500) return { cost: 79, days: 5, label: "In 5–6 days" };
  return                        { cost: 99, days: 7, label: "In 6–8 days" };
}
